package com.tasktop.c2c.server.wiki.domain;

import java.io.Serializable;

/**
 * @author cmorgan (Tasktop Technologies Inc.)
 * 
 */
@SuppressWarnings("serial")
public class PageOutlineItem implements Serializable {
	private String label;
	private String id;
	private String url;

	/**
	 * @return the label
	 */
	public String getLabel() {
		return label;
	}

	/**
	 * @param label
	 *            the label to set
	 */
	public void setLabel(String label) {
		this.label = label;
	}

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}

	/**
	 * @param url
	 *            the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}
}
